<div class="box alt-box">
	<h3>Bundled Environments</h3>
	<p>VVV reads a config file to discover and provision sites named in <code>config/config.yml</code>. If it doesn't exist, it falls back to <code>config/default-config.yml</code>.</p>
	<p>To add, remove, or change sites, modify <code>config/config.yml</code> then reprovision using <code>vagrant up --provision</code> to apply the changes</p>
</div>
